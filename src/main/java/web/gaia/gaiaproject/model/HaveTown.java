package web.gaia.gaiaproject.model;

public class HaveTown {
    String ttno;
    String gameid;
    String userid;
    String ttstate;
    int id;

    public String getTtno() {
        return ttno;
    }

    public void setTtno(String ttno) {
        this.ttno = ttno;
    }

    public String getGameid() {
        return gameid;
    }

    public void setGameid(String gameid) {
        this.gameid = gameid;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getTtstate() {
        return ttstate;
    }

    public void setTtstate(String ttstate) {
        this.ttstate = ttstate;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
